<h3>Производство электросварных спиральношовных труб <br>
 </h3>
<p style="text-align: justify;">
	 Часто для данного вида труб используют название цельнотянутые. Процесс производства осуществляется методом деформации цилиндрической заготовки в высоком температурном режиме.
</p>
<h3>Производство бесшовных горячедеформированных труб <br>
 </h3>
<ul>
	<li>Горячедеформированные трубы не имеют соединительного шва и производятся из цилиндрической монолитной заготовки. <br>
 </li>
</ul>
<ul>
	<li>Стальная заготовка подвергается термообработке в специальной печи. <br>
 </li>
</ul>
<ul>
	<li>Далее производится ее деформация под прошивным прессом и преобразование в гильзу (полый цилиндр). <br>
 </li>
</ul>
<ul>
	<li>Разогретая деформированная заготовка проходит процесс вальцевания и достигает нужных размерных параметров: диаметра и толщины стенок. <br>
 </li>
</ul>
<ul>
	<li>Последний этап - поперечная резка трубы на отрезки заданной длины. <br>
 </li>
</ul>
 <br>
<h3>Производство бесшовных холоднодеформированных труб <br>
 </h3>
<p style="text-align: justify;">
	 Данный вид трубы производится по схожим с предыдущим видом этапам, включая несколько отличий.
</p>
<ul>
	<li>После деформации под прошивным прессом гильза охлаждается. <br>
 </li>
	<li>Вальцевание и порезка металла осуществляется при низкой температуре. <br>
 </li>
	<li>Уже готовое изделие проходит дополнительную процедуру отжигания, нагревается до рекристаллизации и снова охлаждается.&nbsp;&nbsp;</li>
</ul>
 <br>
<p style="text-align: justify;">
	 В производстве профильных труб используются технологии, схожие с теми, которые применяют для труб с круглым сечением. На этапе деформации трубная заготовка приобретает профиль квадратной, прямоугольной или другой формы.
</p>
<p style="text-align: justify;">
	 Для производства профильных труб используют марки стали Ст 2 пс/сп и Ст 08 кп. В нашем ассортименте представлены прямоугольные трубы с размерами сторон от 20х10 до 80х40 мм и квадратные - от 10х10 до 60х60. Все необходимые сертификаты на данный вид продукции доступны для ознакомления по ссылке.
</p>
<p style="text-align: justify;">
	 Высокая технологичность производства позволяет выпускать трубы с широким диапазоном размеров, длиной от 4 до 12 метров.
