<p>Rury wykonane są ze stali walcowanej niskowęglowych marek. Produkowane są również kwadratowe i prostokątne rury spawane profilowane oraz okrągłe rury wodne i gazowe. Oferujemy szeroki wybór, wysoki poziom serwisu usług dodatkowych, europejską jakość produktów. Indywidualnie podchodzimy do każdego zamówienia klienta.  
