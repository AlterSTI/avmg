<h3>Производство электросварных прямошовных труб </h3>
<ul>
	<li>Изначально рулон листовой стали нарезают на полосы продольной резкой.&nbsp;</li>
</ul>
<ul>
	<li>Полученные отрезки объединяют в одну непрерывную. Для этого используют сварку. <br>
 </li>
</ul>
<ul>
	<li>Далее непрерывный отрезок металла деформируют, получая заготовку с круглым сечением и открытым продольным швом. <br>
 </li>
</ul>
<ul>
	<li>Место соединения металла шва сваривается плазмой, лазером и пучками электронов, индукционными токами и дуговой сваркой. <br>
 </li>
</ul>
 <br>
<p style="text-align: justify;">
	 Наиболее частые методы формирования шва - сварка индукционными токами и дуговая сварка вольфрамовыми электродами в среде инертного газа. Преимущество первого метода - в его скорости. Он реализуется примерно в 20 раз быстрее чем дуговая сварка, однако второй метод считается более надежным.
</p>
<ul>
	<li>Сваренную трубу подвергают калибровке в вальцах. <br>
 </li>
</ul>
<ul>
	<li>Образовавшийся шов проходит контроль точности, осуществляемый с помощью ультразвука и вихревых токов. <br>
 </li>
</ul>
<ul>
	<li>Цельная труба нарезается на отрезки заданной длины. <br>
 </li>
</ul>
 <br>
